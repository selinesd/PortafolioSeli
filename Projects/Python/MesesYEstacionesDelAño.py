
print('Por favor, Ingrese un mes del año')
resultado = input()

if resultado == 'enero':
    print('Invierno')
elif resultado == 'febrero':
    print('Invierno')
elif resultado == 'marzo':
    print('Invierno/Primavera')
elif resultado == 'abril':
    print('Primavera')
elif resultado == 'mayo':
    print('Primavera')
elif resultado == 'junio':
    print('Primavera/Verano')
elif resultado == 'julio':
    print('Verano')
elif resultado == 'agosto':
    print('Verano')
elif resultado == 'septiembre':
    print('Verano/Otoño')
elif resultado == 'octubre':
    print('Otoño')
elif resultado == 'noviembre':
    print('Otoño')
elif resultado == 'diciembre':
    print('Otoño/Invierno')